  use {
	  'nvim-treesitter/nvim-treesitter',
	  run = ':TSUpdate'
  }

  -- undotree
  use("mbbill/undotree")

  -- vim fugitive
  use("tpope/vim-fugitive")

  -- lsp zero
  use {
	  'VonHeikemen/lsp-zero.nvim',
	  branch = 'v1.x',
